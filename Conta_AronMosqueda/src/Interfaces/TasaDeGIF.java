        String noO = NoO.getText();
        
        double g = Double.parseDouble(GastosP.getText());
        double tg = Double.parseDouble(TasaGif.getText());
        double b = Double.parseDouble(Base.getText());
        
        
        String sql = "insert into tasagif values(" + noO + "," + g + "," + b + "," + tg + ");";
        
