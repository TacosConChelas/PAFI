    private void calcularTasaDeGIF() {
        try {
            double gastosPresupuestados = Double.parseDouble(jTGastosPresupuestados.getText());
            double baseDeActividad = Double.parseDouble(jTBaseDeActividad.getText());
            double tasaDeGIF = gastosPresupuestados / baseDeActividad;
            jTTasaDeGIF.setText(String.valueOf(tasaDeGIF));
        } catch (NumberFormatException e) {
            jTTasaDeGIF.setText("0");
        }
    }
    
    
    private void generarWord() {
        try {
            XWPFDocument document = new XWPFDocument();
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogTitle("Guardar Word");
            int userSelection = fileChooser.showSaveDialog(this);
            if (userSelection == JFileChooser.APPROVE_OPTION) {
                FileOutputStream out = new FileOutputStream(fileChooser.getSelectedFile() + ".docx");

                // Añadir el título
                XWPFParagraph title = document.createParagraph();
                XWPFRun titleRun = title.createRun();
                titleRun.setText("Tasa de Gastos Indirectos de Fabricación");
                titleRun.setBold(true);
                titleRun.setFontSize(18);
                title.setAlignment(ParagraphAlignment.CENTER);

                // Añadir la información escalonada
                addParagraph(document, "Número de Orden: " + jTNumeroDeOrden.getText());
                addParagraph(document, "Fecha: " + jTFecha.getText());
                addParagraph(document, "Departamento: " + jTDepartamento.getText());
                addParagraph(document, "Número de Pedido: " + jTNumeroDePedido.getText());
                addParagraph(document, "");
                addParagraph(document, "Gastos Presupuestados: " + jTGastosPresupuestados.getText());
                addParagraph(document, "Base de Actividad: " + jTBaseDeActividad.getText());
                addParagraph(document, "Tasa de GIF: " + jTTasaDeGIF.getText());

                document.write(out);
                out.close();
                JOptionPane.showMessageDialog(this, "Word generado exitosamente.");
            }
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al generar el archivo Word.");
        }
    }

    private void addParagraph(XWPFDocument document, String text) {
        XWPFParagraph paragraph = document.createParagraph();
        XWPFRun run = paragraph.createRun();
        run.setText(text);
    }

