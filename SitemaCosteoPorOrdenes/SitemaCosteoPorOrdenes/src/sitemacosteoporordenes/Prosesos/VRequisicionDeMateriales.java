    private void calcularCostos() {
        calcularCostoUnitario(jTCantidad1, jTCostoUnitario1, jTCostoTotal1);
        calcularCostoUnitario(jTCantidad2, jTCostoUnitario2, jTCostoTotal2);
        calcularCostoUnitario(jTCantidad3, jTCostoUnitario3, jTCostoTotal3);
        calcularCostoUnitario(jTCantidad4, jTCostoUnitario4, jTCostoTotal4);

        double total = calcularTotal();
        jTTotal.setText(String.valueOf(total));
    }

    private void calcularCostoUnitario(javax.swing.JTextField jTCantidad, javax.swing.JTextField jTCostoUnitario, javax.swing.JTextField jTCostoTotal) {
        try {
            double cantidad = Double.parseDouble(jTCantidad.getText());
            double costoUnitario = Double.parseDouble(jTCostoUnitario.getText());
            double costoTotal = cantidad * costoUnitario;
            jTCostoTotal.setText(String.valueOf(costoTotal));
        } catch (NumberFormatException e) {
            jTCostoTotal.setText("0");
        }
    }

    private double calcularTotal() {
        double total1 = obtenerValor(jTCostoTotal1);
        double total2 = obtenerValor(jTCostoTotal2);
        double total3 = obtenerValor(jTCostoTotal3);
        double total4 = obtenerValor(jTCostoTotal4);
        return total1 + total2 + total3 + total4;
    }

    private double obtenerValor(javax.swing.JTextField jTCostoTotal) {
        try {
            return Double.parseDouble(jTCostoTotal.getText());
        } catch (NumberFormatException e) {
            return 0;
        }
    }

    private void generarWord() {
        try {
            XWPFDocument document = new XWPFDocument();
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogTitle("Guardar Word");
            int userSelection = fileChooser.showSaveDialog(this);
            if (userSelection == JFileChooser.APPROVE_OPTION) {
                FileOutputStream out = new FileOutputStream(fileChooser.getSelectedFile() + ".docx");

                // Añadir el título
                XWPFParagraph title = document.createParagraph();
                XWPFRun titleRun = title.createRun();
                titleRun.setText("Requisición de Materiales");
                titleRun.setBold(true);
                titleRun.setFontSize(18);
                title.setAlignment(ParagraphAlignment.CENTER);

                // Añadir la información escalonada
                addParagraph(document, "Número de Requisición: " + jTNumeroRequisicion.getText());
                addParagraph(document, "Departamento: " + jTDepartamento.getText());
                addParagraph(document, "Número de Orden: " + jTNumeroOrden.getText());
                addParagraph(document, "Fecha: " + jTFecha.getText());
                addParagraph(document, "Total: " + jTTotal.getText());
                addParagraph(document, "");

                addParagraph(document, "Cantidad: " + jTCantidad1.getText() + " - Descripción: " + jTDescripcion1.getText() + " - Costo Unitario: " + jTCostoUnitario1.getText() + " - Costo Total: " + jTCostoTotal1.getText());
                addParagraph(document, "Cantidad: " + jTCantidad2.getText() + " - Descripción: " + jTDescripcion2.getText() + " - Costo Unitario: " + jTCostoUnitario2.getText() + " - Costo Total: " + jTCostoTotal2.getText());
                addParagraph(document, "Cantidad: " + jTCantidad3.getText() + " - Descripción: " + jTDescripcion3.getText() + " - Costo Unitario: " + jTCostoUnitario3.getText() + " - Costo Total: " + jTCostoTotal3.getText());
                addParagraph(document, "Cantidad: " + jTCantidad4.getText() + " - Descripción: " + jTDescripcion4.getText() + " - Costo Unitario: " + jTCostoUnitario4.getText() + " - Costo Total: " + jTCostoTotal4.getText());

                document.write(out);
                out.close();
                JOptionPane.showMessageDialog(this, "Word generado exitosamente.");
            }
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al generar el archivo Word.");
        }
    }

    private void addParagraph(XWPFDocument document, String text) {
        XWPFParagraph paragraph = document.createParagraph();
        XWPFRun run = paragraph.createRun();
        run.setText(text);
    }
    
